package main;

public class Mp3TagException extends Exception {

	public Mp3TagException() {
		super();
		
	}

	public Mp3TagException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	
	}

	public Mp3TagException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public Mp3TagException(String message) {
		super(message);
		
	}

	public Mp3TagException(Throwable cause) {
		super(cause);
		
	}

	
}
